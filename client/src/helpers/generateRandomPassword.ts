export const generateRandomPassword = () => {
  const getRandomWord = (words) => {
    const randomIndex = Math.floor(Math.random() * words.length);
    return words[randomIndex];
  };

  const firstWord = getRandomWord(wordsArray);
  const secondWord = getRandomWord(wordsArray);
  const combinedWords = `${firstWord.charAt(0).toUpperCase()}${firstWord.slice(
    1,
  )}${secondWord.charAt(0).toUpperCase()}${secondWord.slice(1)}`;

  const randomNumbers = Array.from({ length: 2 }, () =>
    Math.floor(Math.random() * 10),
  ).join("");

  const specialCharacters = ["!", "@", "#", "$", "%", "&"];

  const randomSpecialChar =
    specialCharacters[Math.floor(Math.random() * specialCharacters.length)];

  const password = `${combinedWords}${randomNumbers}${randomSpecialChar}`;

  return password;
};

